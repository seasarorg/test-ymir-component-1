package org.seasar.cms.ymir.extension.creator.impl;

public class Merge3BaseBase {

    protected String hoe_;

    protected String fuga_;

    public String getHoe() {
        return hoe_;
    }

    public int add(int f1, int f2) {
        return f1 + f2;
    }

    public int inc(int f1, int f2) {
        return f1 + 1;
    }

    public void setFuga(String fuga) {
        fuga_ = fuga;
    }
}
